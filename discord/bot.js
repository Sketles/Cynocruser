function validateConfig() {
    const required = ['DISCORD_TOKEN', 'CLIENT_ID'];
    const missing = required.filter(key => !process.env[key]);

    if (missing.length > 0) {
        console.error('‚ùå Variables faltantes:', missing.join(', '));
        process.exit(1);
    }

    console.log('‚úÖ Configuraci√≥n validada');
}

