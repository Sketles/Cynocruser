// INTERFAZ CLI
// ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
async function startCLI() {
    await initialize();

    const rl = readline.createInterface({
        input: process.stdin,
        output: process.stdout
    });

    const characterName = cassette.engram.identity?.name || 'AI';

    const prompt = () => {
        process.stdout.write('\n' + colors.user.bold('T√∫ ') + colors.dim('‚Ä∫ '));

        rl.question('', async (input) => {
            const userInput = input.trim();

            if (!userInput) {
                prompt();
                return;
            }

            // Comandos
            if (userInput.toLowerCase() === '/salir' || userInput.toLowerCase() === '/exit') {
                console.log('\n' + colors.dim('üëã ¬°Hasta luego!\n'));
                rl.close();
                process.exit(0);
            }

            if (userInput.toLowerCase() === '/estado') {
                showState();
                prompt();
                return;
            }

            if (userInput.toLowerCase() === '/clear') {
                showBanner();
                prompt();
                return;
            }

            // Generar respuesta con spinner
            const spinner = ora({
                text: colors.dim('pensando...'),
                color: 'magenta',
                spinner: 'dots'
            }).start();

            try {
                const response = await generateResponse(userInput);
                spinner.stop();

                // Mostrar respuesta con estilo
                console.log('\n' + colors.ai.bold(characterName) + colors.dim(' ‚Ä∫ ') + response);

            } catch (error) {
                spinner.fail(colors.error('Error: ' + error.message));
            }

            prompt();
        });
    };

    prompt();
}

