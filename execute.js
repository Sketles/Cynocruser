async function startCLI() {
    await initialize();

    const rl = readline.createInterface({
        input: process.stdin,
        output: process.stdout
    });


    const prompt = () => {
        rl.question('T√∫: ', async (input) => {
            const userInput = input.trim();

            if (!userInput) {
                prompt();
                return;
            }

            if (userInput.toLowerCase() === 'salir' || userInput.toLowerCase() === 'exit') {
                console.log('\nüëã ¬°Hasta luego!\n');
                rl.close();
                process.exit(0);
            }

            if (userInput.toLowerCase() === 'estado') {
                const state = psiOrgan.soma.getState();
                console.log('\nüß† Estado Œ®-Organ:');
                console.log(`   Energ√≠a: ${state.tanks.energia.toFixed(1)}`);
                console.log(`   Integridad: ${state.tanks.integridad.toFixed(1)}`);
                console.log(`   Afiliaci√≥n: ${state.tanks.afiliacion.toFixed(1)}`);
                console.log(`   Certeza: ${state.tanks.certeza.toFixed(1)}`);
                console.log(`   Competencia: ${state.tanks.competencia.toFixed(1)}`);
                console.log('');
                prompt();
                return;
            }

            try {
                const response = await generateResponse(userInput);
                console.log(`\nü§ñ ${cassette.engram.identity?.name || 'AI'}: ${response}\n`);
            } catch (error) {
                console.error(`\n‚ùå Error: ${error.message}\n`);
            }

            prompt();
        });
    };

    prompt();
}

