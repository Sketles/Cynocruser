async function generateResponse(userMessage, userId = 'cli-user') {
    // Procesar input con Œ®-Organ
    const psiState = psiOrgan.process(userMessage, { userId });

    // Construir system prompt
    const systemPrompt = buildSystemPrompt(cassette, userId, psiState);

    // DEBUG MODE: Muestra prompt completo y tokens cuando DEBUG=true
    const isDebug = process.env.DEBUG === 'true';

    if (isDebug) {
        console.log('\n‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê SYSTEM PROMPT (DEBUG) ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê');
        console.log(systemPrompt);  // Prompt COMPLETO
        console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê');
        console.log(`üìä Total caracteres: ${systemPrompt.length}`);
        console.log(`üìä Tokens estimados: ~${Math.ceil(systemPrompt.length / 4)}\n`);
    }

    // Agregar al historial (Formato OpenAI)
    conversationHistory.push({ role: 'user', content: userMessage });

    // Mantener historial corto
    if (conversationHistory.length > 20) {
        conversationHistory = conversationHistory.slice(-20);
    }

    // Llamar al Cliente AI
    const aiText = await aiClient.generateResponse(
        conversationHistory,
        systemPrompt,
        {
            temperature: 0.9,
            topP: 0.95,
            maxOutputTokens: 150
        }
    );

    // Agregar respuesta al historial
    conversationHistory.push({ role: 'assistant', content: aiText });

    return aiText;
}

